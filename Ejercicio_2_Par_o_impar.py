#Número es mayor que cero y es par
numero=float(input("\nIngrese un número por favor: "))
if numero>=0 and numero%2==0:
    print("\n",numero,"Este número es mayor que cero y es par")
else:
   print("\n",numero,"Este número no es mayor que cero y no es par")
   
#Número es par o impar
numero=float(input("\nIngrese un número por favor: "))
if numero%2==0:
    print("\n",numero,"Este número es par")
else:
    print("\n",numero,"Este número es impar")
